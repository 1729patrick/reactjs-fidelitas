type Order = 'asc' | 'desc';

type Props = {
  hidden?: Boolean;
};

function getComparator<Key extends keyof any>(
  order: Order,
  orderBy: Key,
): (
  a: { [key in Key]: number | string },
  b: { [key in Key]: number | string },
) => number {
  return order === 'desc'
    ? (a, b) => descendingComparator(a, b, orderBy)
    : (a, b) => -descendingComparator(a, b, orderBy);
}

function stableSort<T>(array: T[], comparator: (a: T, b: T) => number) {
  const stabilizedThis = array.map((el, index) => [el, index] as [T, number]);
  stabilizedThis.sort((a, b) => {
    const order = comparator(a[0], b[0]);
    if (order !== 0) return order;
    return a[1] - b[1];
  });
  return stabilizedThis.map(el => el[0]);
}

export default function CollapsibleTable(props: Props) {
  const { hidden } = props;
  const classes = useStyles();
  const [order, setOrder] = React.useState<Order>('asc');
  const [orderBy, setOrderBy] = React.useState<keyof Data>('calories');

  const createSortHandler =
    (property: keyof Data) => (event: React.MouseEvent<unknown>) => {
      handleRequestSort(event, property);
    };

  const handleRequestSort = (
    event: React.MouseEvent<unknown>,
    property: keyof Data,
  ) => {
    const isAsc = orderBy === property && order === 'asc';
    setOrder(isAsc ? 'desc' : 'asc');
    setOrderBy(property);
  };
  return (
    <TableContainer component={Paper} style={{ display: hidden && 'none' }}>
      <Table aria-label="collapsible table">
        <TableHead>
          <TableRow>
            <TableCell padding={'default'} sortDirection={false} />
            <TableCell padding={'default'} sortDirection={false}>
              Produtos
            </TableCell>
            <TableCell align="right" padding={'default'} sortDirection={false}>
              <TableSortLabel
                active={orderBy === 'calories'}
                direction={orderBy === 'calories' ? order : 'asc'}
                onClick={createSortHandler('calories')}>
                Primeiro nome
                {orderBy === 'calories' ? (
                  <span className={classes.visuallyHidden}>
                    {order === 'desc'
                      ? 'sorted descending'
                      : 'sorted ascending'}
                  </span>
                ) : null}
              </TableSortLabel>
            </TableCell>
            <TableCell align="right" padding={'default'} sortDirection={false}>
              Fat&nbsp;(g)
            </TableCell>
            <TableCell align="right" padding={'default'} sortDirection={false}>
              Carbs&nbsp;(g)
            </TableCell>
            <TableCell align="right" padding={'default'} sortDirection={false}>
              Protein&nbsp;(g)
            </TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {rows.map(row => (
            <Row key={row.name} row={row} />
          ))}
        </TableBody>
      </Table>
    </TableContainer>
  );
}
